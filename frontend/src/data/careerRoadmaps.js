export function recommendedCategory(stream, className) {
  if (className === "9th" || className === "10th") return "foundation";
  if (stream === "Arts") return "arts";
  if (stream === "Science") return "science";
  return "all";
}

export function roadmapsForCategory(categoryId) {
  if (categoryId === "all") return CAREER_ROADMAPS;
  return CAREER_ROADMAPS.filter((r) => r.category === categoryId);
}

export function progressStorageKey(roadmapId) {
  return `apex-career-progress-${roadmapId}`;
}
